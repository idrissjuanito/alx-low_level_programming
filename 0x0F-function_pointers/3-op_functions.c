#include "3-calc.h"

/**
 * op_add - adds two numbers together
 *
 * @a: first argument to add
 * @b: second argument to add
 *
 * Return: sum of the two arguments
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_mul - multiply two numbers together
 *
 * @a: first argument to add
 * @b: second argument to add
 *
 * Return: product of the two arguments
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_sub - substraction two numbers together
 *
 * @a: first argument to add
 * @b: second argument to add
 *
 * Return: remainder of the two arguments
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_div - division two numbers together
 *
 * @a: first argument to add
 * @b: second argument to add
 *
 * Return: factor of the two arguments
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus two numbers together
 *
 * @a: first argument to add
 * @b: second argument to add
 *
 * Return: results of the two arguments
 */
int op_mod(int a, int b)
{
	return (a % b);
}
